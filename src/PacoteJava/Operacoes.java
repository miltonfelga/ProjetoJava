
package PacoteJava;
public class Operacoes {

    public static double soma (double v1, double v2){
         return(v1+v2);
     }
    public static double subtracacao(double v1, double v2){
        return(v1-v2);
    }
    public static double multiplicacacao(double v1,double v2){
        return(v1*v2);
    }
    public static double divisao(double v1, double v2){
        return(v1/v2);
    }
    }
    
